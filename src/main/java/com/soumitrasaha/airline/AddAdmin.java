    public void AutoID() throws SQLException{
        try {
            Connection con;
            PreparedStatement pre;
            
            Class.forName("com.mysql.jdbc.Driver");
            // Connecting to the DB:
            con = DriverManager.getConnection("jdbc:mysql://localhost/airline_project", "root", "admin");
            pre = con.prepareStatement("select MAX(admin_id) from admin"); // We can write SQL Query
            ResultSet res = pre.executeQuery();
            res.next();
            if(res.getString("MAX(admin_id)") == null){
                admin_id.setText("AD001");
            }
            else{
                long id = Long.parseLong(res.getString("MAX(admin_id)").substring(2));
                id++;
                admin_id.setText("AD"+String.format("%03d", id));
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

