    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // Geting the Attributes:
            String adminId = admin_id.getText();
            String firstName = first_name.getText();
            String lastName = last_name.getText();
            String userName = user_name.getText();
            String pword = password.getText();
                    
            // Requirements:
            Connection con;
            PreparedStatement pre;
            
            // Preparing the JDBC Driver
            Class.forName("com.mysql.jdbc.Driver");
            // Connecting DB with Driver:
            con = DriverManager.getConnection("jdbc:mysql://localhost/airline_project", "root", "admin");
            // We can write SQL Query for inserting the data into the table:
            pre = con.prepareStatement("insert into admin(admin_id, first_name, last_name, user_name, password)values(?,?,?,?,?)");
            // Seting the Attributes:
            pre.setString(1, adminId);
            pre.setString(2, firstName);
            pre.setString(3, lastName);
            pre.setString(4, userName);
            pre.setString(5, pword);
            // Executing the Update:
            pre.executeUpdate();
            
            // Message:
            JOptionPane.showMessageDialog(null, "Admin added successfully");
            
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(AddAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


