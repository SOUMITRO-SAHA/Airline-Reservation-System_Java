    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            // Collecting all the data:
            String flightId = flight_id.getText();
            String flightName = flight_name.getText();
            String _arrival = arrival.getText();
            String _departure = departure.getText();
            String _duration = duration.getText();
            String _seats = seats.getText();
            String _fare = fare.getText();
            
            // Formating the Date:
            DateFormat df = new SimpleDateFormat("dd MMMM yyyy");
            String _date = df.format(date.getDate());
            
            Connection con;
            PreparedStatement pre;
            Class.forName("com.mysql.jdbc.Driver"); // Preparing the JDBC Driver
            // Connecting to the DB:
            con = DriverManager.getConnection("jdbc:mysql://localhost/airline_project", "root", "admin");
            // We can write SQL Query for inserting the data into the table:
            pre = con.prepareStatement("insert into flights(flight_id, flight_name, arrival, departure, duration, seats, fare, flight_date)values(?,?,?,?,?,?,?,?)");
            // Giving the Inputs:
            pre.setString(1, flightId);
            pre.setString(2, flightName);
            pre.setString(3, _arrival);
            pre.setString(4, _departure);
            pre.setString(5, _duration);
            pre.setString(6, _seats);
            pre.setString(7, _fare);
            pre.setString(8, _date);
            
            // Now, Execute the Update:
            pre.executeUpdate();
            
            // Massage:
            JOptionPane.showMessageDialog(null, "Flight Added Successfully");
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(AddFlight.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

